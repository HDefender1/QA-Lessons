import java.util.*;

public class Main {
    public static void main(String[] args) {
      
      int a = 30;
      int b = 50;
      int sum = 0;
      
      if (a < b)
      {
      for(int i = a; i <= b; i++)
      {
        sum = sum + i;
      
       }
      }
      System.out.println("Sum of the range is " + sum);
  }
}